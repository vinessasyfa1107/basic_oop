class LuasSegitiga {
    public static void main(String [] args){
        int a;
        int t;
        double luas;


        //Mendeklarasi nilai alas dan tinggi
        a = 4;
        t = 5;

        luas = (a*t)/2;

        System.out.println("Alas defaultnya adalah 4");
        System.out.println("\nTinggi defaultnya adalah 5");
        System.out.println("\nRumus Segitiga adalah (a*t)/2");
        System.out.println("\n--> " + luas);
    }
}
